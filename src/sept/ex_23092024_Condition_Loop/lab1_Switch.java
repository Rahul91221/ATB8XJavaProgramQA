public class lab1_Switch {
    public static void main(String[] args) {

        int day = 1;
        switch (day){
            case 1:
                System.out.println("Mon");
                break;

            case 2:
                System.out.println("Mon");
                break;
            case 3:
                System.out.println("Mon");
                break;
            default:
                System.out.println("No idea, what day its");
                break;
        }
         System.out.println("End of loop");
    }
}
